package com.laolang.junitstudy.junit_study;

/**
 * The Class Cal.
 */
public class Cal {

	/**
	 * Adds the.
	 *
	 * @param a
	 *            the a
	 * @param b
	 *            the b
	 * @return a + b
	 */
	public int add(int a, int b) {
		return a + b;
	}

	/**
	 * Minus.
	 *
	 * @param a
	 *            the a
	 * @param b
	 *            the b
	 * @return a - b
	 */
	public int minus(int a, int b) {
		return a - b;
	}

	/**
	 * Mul.
	 *
	 * @param a
	 *            the a
	 * @param b
	 *            the b
	 * @return a * b
	 */
	public int mul(int a, int b) {
		return a * b;
	}

	/**
	 * Divided.
	 *
	 * @param a
	 *            the a
	 * @param b
	 *            the b
	 * @return a / b
	 */
	public int divided(int a, int b) {
		return a / b;
	}
}
